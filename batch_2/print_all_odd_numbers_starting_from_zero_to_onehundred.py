# Initialize the variable odd_num to 0
odd_num = 0

# Loop while odd_num is less than or equal to 100
while odd_num <= 100:
    # Check if odd_num is an odd number
    if odd_num % 2 != 0:
        # Print the odd number followed by a space
        print(odd_num, end=' ')
    # Increment odd_num variable by 1
    odd_num += 1

# Print a newline character at the end
print()
